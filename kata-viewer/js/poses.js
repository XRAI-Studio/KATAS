// Pose library — pure data + pure functions. No Three.js, no DOM (Node-importable).
//
// Conventions:
//  - Character faces +Z at root.ry = 0. Character's left side is +X.
//  - A pose is { root: {x, y, z, ry}, joints: { name: {x, y, z} } } with Euler XYZ radians.
//    root.x/z are small in-stance offsets (embusen placement is applied separately);
//    root.y is an offset from standing hip height (negative = lower stance).
//  - Joint zero = standing straight, arms hanging at sides.
//  - Rotation signs: joint +x rotates the hanging limb backward (-Z), -x forward (+Z);
//    knee flexion is +x; elbow flexion is -x.

export const JOINT_NAMES = [
  'hips', 'spine', 'chest', 'neck', 'head',
  'shoulderL', 'elbowL', 'wristL', 'shoulderR', 'elbowR', 'wristR',
  'hipL', 'kneeL', 'ankleL', 'hipR', 'kneeR', 'ankleR',
];

export function emptyPose() {
  const joints = {};
  for (const n of JOINT_NAMES) joints[n] = { x: 0, y: 0, z: 0 };
  return { root: { x: 0, y: 0, z: 0, ry: 0 }, joints };
}

export const BASE_POSE = emptyPose();

// Merge partial poses left-to-right onto a fresh base. Later partials override
// per joint (whole-joint replacement) and per root field.
export function composePose(...partials) {
  const out = emptyPose();
  for (const p of partials) {
    if (!p) continue;
    if (p.root) for (const k of ['x', 'y', 'z', 'ry']) {
      if (p.root[k] !== undefined) out.root[k] = p.root[k];
    }
    if (p.joints) for (const [name, rot] of Object.entries(p.joints)) {
      out.joints[name] = { x: rot.x ?? 0, y: rot.y ?? 0, z: rot.z ?? 0 };
    }
  }
  return out;
}

// Mirror a partial pose across the sagittal plane: swap L/R joints,
// negate y/z rotation components, negate root.x and root.ry.
export function mirrorPose(p) {
  const out = { };
  if (p.root) out.root = {
    ...(p.root.x !== undefined ? { x: -p.root.x } : {}),
    ...(p.root.y !== undefined ? { y: p.root.y } : {}),
    ...(p.root.z !== undefined ? { z: p.root.z } : {}),
    ...(p.root.ry !== undefined ? { ry: -p.root.ry } : {}),
  };
  if (p.joints) {
    out.joints = {};
    for (const [name, rot] of Object.entries(p.joints)) {
      const swapped = name.endsWith('L') ? name.slice(0, -1) + 'R'
        : name.endsWith('R') ? name.slice(0, -1) + 'L' : name;
      out.joints[swapped] = { x: rot.x ?? 0, y: -(rot.y ?? 0), z: -(rot.z ?? 0) };
    }
  }
  return out;
}

