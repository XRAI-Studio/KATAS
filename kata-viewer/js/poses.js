// ---------------------------------------------------------------------------
// Assemble POSES: L/R variants generated by mirroring.
// ---------------------------------------------------------------------------
export const POSES = {};

for (const [name, pose] of Object.entries(STANCES_BASE)) {
  if (name.endsWith('L')) {
    POSES[name] = pose;
    POSES[name.slice(0, -1) + 'R'] = mirrorPose(pose);
  } else {
    POSES[name] = pose;
  }
}
for (const [name, pose] of Object.entries(ARMS_RIGHT)) {
  POSES[name + 'R'] = pose;
  POSES[name + 'L'] = mirrorPose(pose);
}
for (const [name, pose] of Object.entries(ARMS_BOTH)) {
  POSES[name] = pose;
  if (name.endsWith('R')) POSES[name.slice(0, -1) + 'L'] = mirrorPose(pose);
}
for (const [name, pose] of Object.entries(LEGS_RIGHT)) {
  POSES[name + 'R'] = pose;
  POSES[name + 'L'] = mirrorPose(pose);
}
